// == vs ===

let num1 = "7";
let num2 = 7;

console.log(num1 == num2); // == check only value
console.log(num1 === num2); // === check  both the value & datatype

// != vs !==

console.log(num1 != num2); // !== check only value
console.log(num1 !== num2); // !== check both the value & datatype
